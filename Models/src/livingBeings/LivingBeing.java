package src.livingBeings;



public class LivingBeing {
    
    protected boolean isOrganized = true;
    protected boolean hasHomeoStasis = true;
    protected boolean hasMetabolism = true;
    protected boolean willGrow = true;
    protected boolean canAdapt = true;
    protected boolean canRespond = true;
    protected boolean canReproduce = true;
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    public boolean isOrganized() {
        return isOrganized;
    }
    public void setOrganized(boolean isOrganized) {
        this.isOrganized = isOrganized;
    }
    public boolean isHasHomeoStasis() {
        return hasHomeoStasis;
    }
    public void setHasHomeoStasis(boolean hasHomeoStasis) {
        this.hasHomeoStasis = hasHomeoStasis;
    }
    public boolean isHasMetabolism() {
        return hasMetabolism;
    }
    public void setHasMetabolism(boolean hasMetabolism) {
        this.hasMetabolism = hasMetabolism;
    }
    public boolean isWillGrow() {
        return willGrow;
    }
    public void setWillGrow(boolean willGrow) {
        this.willGrow = willGrow;
    }
    public boolean isCanAdapt() {
        return canAdapt;
    }
    public void setCanAdapt(boolean canAdapt) {
        this.canAdapt = canAdapt;
    }
    public boolean isCanRespond() {
        return canRespond;
    }
    public void setCanRespond(boolean canRespond) {
        this.canRespond = canRespond;
    }
    public boolean isCanReproduce() {
        return canReproduce;
    }
    public void setCanReproduce(boolean canReproduce) {
        this.canReproduce = canReproduce;
    }
    
    
}
